// Declaração das variáveis numéricas
let num_A = 10;
let num_B = 5;

// Adição
let soma = num_A + num_B;
console.log(`Resultado da adição entre ${num_A} e ${num_B} = ${soma}`);

// Subtração
let subtracao = num_A - num_B;
console.log(`Resultado da subtração entre ${num_A} e ${num_B} = ${subtracao}`);

// Multiplicação
let multiplicacao = num_A * num_B;
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} = ${multiplicacao}`);

// Divisão
let divisao = num_A / num_B;
console.log(`Resultado da divisão entre ${num_A} e ${num_B} = ${divisao}`);
